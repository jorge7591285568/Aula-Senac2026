# Programa simples: calcular a média de dois números

# Define as variáveis com os valores
numero1 = 10
numero2 = 20

# Calcula a soma dos dois números
soma = numero1 + numero2

# Calcula a média dividindo a soma por 2
media = soma / 2

# Mostra o resultado na tela
print(f"A média entre {numero1} e {numero2} é: {media}")

